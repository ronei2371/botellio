def buscar_configuracao(resina, marca, modelo):
    """Busca configuração específica para resina + impressora"""
    resina_upper = resina.upper().replace("+", "").replace(" ", "_")
    marca_upper = marca.upper()
    modelo_upper = modelo.upper()
    
    if resina_upper in CONFIGURACOES_QUANTON3D:
        for config in CONFIGURACOES_QUANTON3D[resina_upper]:
            if marca_upper in config["marca"].upper() and modelo_upper in config["modelo"].upper():
                return config
    
    return None